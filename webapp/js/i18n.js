// Full internationalisation for the RECARO R7 experience.
// t(key, params) resolves the current language; setUILang switches it.
// Elements with data-i18n / data-i18n-ph / data-i18n-title are auto-updated
// by applyI18n().

let current = "en-US";

export function setUILang(code) {
  current = STRINGS[code] ? code : "en-US";
  document.documentElement.lang = current.split("-")[0];
  document.body.dir = current === "ar-SA" ? "rtl" : "ltr";
  applyI18n();
}

export function getUILang() { return current; }

export function t(key, params) {
  let s = (STRINGS[current] && STRINGS[current][key]) || STRINGS["en-US"][key] || key;
  if (params) for (const [k, v] of Object.entries(params)) s = s.replaceAll(`{${k}}`, v);
  return s;
}

export function applyI18n() {
  document.querySelectorAll("[data-i18n]").forEach((el) => { el.textContent = t(el.dataset.i18n); });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => { el.placeholder = t(el.dataset.i18nPh); });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => { el.title = t(el.dataset.i18nTitle); });
}

